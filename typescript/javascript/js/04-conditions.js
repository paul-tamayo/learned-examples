// Operador ternario
let persona = ["Diego", 35];
console.log(persona[1] >= 18 ? "Es mayor de edad" : "Es menor de edad.");

// Diferencia entre == y ===, != y !==
let age1 = "80";
let age2 = 80;
console.log(age1 == age2); // No evalua el tipo de dato
console.log(age1 === age2); // Evalua el contenido y el tipo de dato
